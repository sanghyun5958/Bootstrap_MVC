        <h4><span>Description</span></h4>
      </div>
      <p><span class="highlight default">1</span> The Asian and Pacific Training Centre for ICT for Development (APCICT) organized a session on “Women ICT Frontier Initiative: ICT Enabled Women Entrepreneurship” at the Second Regional Central Asia Research and Education Network (CAREN) Conference on 26 April 2017 in cooperation with CAREN. Attended by more than 120 researchers, academics and students from Kazakhstan, Kyrgyzstan, Tajikistan, Turkmenistan and Uzbekistan, the CAREN conference provided a platform for a dialogue on how ICT can be leveraged for research and higher education. </p>
      <p><span class="highlight default">2</span> APCICT in partnership with the Academy of Public Administration under President of Kyrgyz Republic (APAPKR), and Central Asian Research and Education Network (CAREN) co-organized the CIS Sub-Regional Workshop and the Kyrgyzstan National Launch of Women ICT Frontier Initiative (WIFI) on 24-27 April 2017 at the Academy of Public Administration in Bishkek.</p>
      <p><span class="highlight default">3</span> The national launch was attended by around 150 representatives from government ministries including the President’s Office, Ministry of Finance, Ministry of Economy, Chamber of Commerce, training institutes, civil society, women entrepreneur associations, international organizations, media and other relevant stakeholders in entrepreneurship and women’s empowerment.</p>
      
