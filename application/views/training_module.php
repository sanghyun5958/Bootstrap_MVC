                            <h1 class="text-primary">WIFI Training Modules</h1>
                            <br>
                            <p>WIFI includes a comprehensive and integrated ICT and entrepreneurship training programme, as well as tools and learning platforms.</p>
                           
                            <h3><b> WIFI Training Programme </b> </h3>
                            
                            <p> The WIFI Training Programme has two target audiences: 1) women entrepreneurs; and 2) government leaders and policymakers. The WIFI training curriculum consists of core content modules for all participants and specialized modules for women entrepreneurs and policymakers.  With this approach, five WIFI modules are developed.</p>
                            <h3> Core Modules</h3>
                            <p>  -Module 1 on Women's Empowerment, SDGs and ICT</p>
                            <p>  -Module 2 on Enabling Role of ICT for Women Entrepreneurs </p>

                            <h3> Specialized Modules for Women Entrepreneurs</h3>
                            <p>  -Module 1 on Planning a Business Using ICT</p>
                            <p> -Module 2 on Managing a Business Using ICT </p>

                            <h3> Specialized Module for Policymakers </h3>
                            <p> -An Enabling Environment for Women Entrepreneurs </p>
                            <p> The WIFI training curriculum was developed to encompass the spectrum of themes and policy issues that affect women’s entrepreneurship. It will continue to be expanded to cover new and emerging issues on the theme of ICT and women’s economic empowerment.</p>

