        <h4><span>Description</span></h4>
      </div>
      <p><span class="highlight default">1</span> The Asian and Pacific Training Centre for ICT for Development, in cooperation with the ICT Division of Bangladesh, launched the Women ICT Frontier Initiative (WIFI) in Dhaka on 3-5 July 2017.</p>
      <p><span class="highlight default">2</span> The implementation of the WIFI programme in the country is undertaken through the cooperation and partnership of stakeholders from government, civil society, and women’s groups and entrepreneur associations. Implementing partners include the Bangladesh Computer Council, the Department of ICT under the ICT Division, Bangladesh Institute of ICTs in Development, and Bangladesh Women in Information Technology.</p>
      <p><span class="highlight default">3</span> The ceremonial launch in Dhaka was held on 5 July and graced by high-level representatives from the government, including the Speaker of the Bangladesh Parliament and State Ministers from the Ministry of Posts, Telecommunications and Information Technology and the Ministry of Women and Child Affairs.</p>
