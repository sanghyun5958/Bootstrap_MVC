      <p><span class="highlight default">1</span> The Asian and Pacific Training Centre for ICT for Development (APCICT/ESCAP), in cooperation with Sri Padmavati Mahila Visvaidyalayam (Women’s University) launched the Women ICT Frontier Initiative (WIFI) in Tirupati, Andhra Pradesh on 19 February 2018. </p>
      <p><span class="highlight default">2</span> Following the launch, a WIFI Training of Trainers (TOT) for policymakers and women entrepreneurs was organized on 20-22 February 2018. The Training of Trainers aimed to build a network of resource persons and trainers who can help implementing the WIFI programme at the state and local levels.</p>
