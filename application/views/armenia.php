        <h4><span>Description</span></h4>
      </div>
      <p><span class="highlight default">1</span> The Asian and Pacific Training Centre for ICT for Development (APCICT/ESCAP), in cooperation with the Civil Service Council of the Republic of Armenia, and Armenian Young Women’s Association launched the Women ICT Frontier Initiative (WIFI) in Yerevan on 29 September 2017. </p>
      <p><span class="highlight default">2</span> The WIFI launch was attended by high-level government officials and representatives of the Civil Service Council of the Republic of Armenia, relevant ministries as well as women entrepreneur associations, national government agencies, and other stakeholders in the areas of entrepreneurship and women’s empowerment.</p>
      <p><span class="highlight default">3</span> Prior to the launch, a WIFI Training of Trainers (TOT) for policymakers and women entrepreneurs was organized on 26-28 September 2017. The TOT aimed to expand the pool of trainers and resource persons in Armenia and who can help bring the WIFI training programme at the national and community levels.</p>
      
