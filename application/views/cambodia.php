        <p><span class="highlight default">1</span> The Asian and Pacific Training Centre for ICT for Development, in collaboration with the National One Village One Product (OVOP) Committee under the Council for Agricultural and Rural Development (CARD), and with support from the Ministry of Women’s Affairs of Cambodia, co-organized the national launch of Women ICT Frontier Initiative (WIFI) on 19-21 December 2016 at the Office of the Council of Ministers in Phnom Penh, Cambodia. </p>
        <p><span class="highlight default">2</span> The inaugural launch of WIFI in Cambodia, which took place on 21 December 2016, was attended by around 150 representatives from government ministries, training institutes, civil society, women entrepreneur associations, international organizations, media and other relevant stakeholders in entrepreneurship and women’s empowerment.</p>
       
       
       <p><span class="highlight default">3</span> Prior to the launch event, Training of Trainers (ToTs) in two tracks - for policymakers and women entrepreneurs - were held on 19–21 December. The session for policymaker will focus on the importance of a gender-responsive government (including gender-sensitive policies and services) in order create an inclusive and accessible enabling environment for them. The TOT session for women entrepreneurs introduced the key concepts of women's empowerment and entrepreneurship; present challenges faced by women ICT; and the role of ICT in supporting women’s entrepreneurship.  The TOTs will help expand the pool of national resource persons in Cambodia who can deliver WIFI training at the community level.</p>
