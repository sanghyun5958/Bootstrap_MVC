              <p>APCICT’s third flagship is called Women ICT Frontier Initiative (WIFI). It aims to promote women’s economic empowerment through ICT capacity development. The programme has been developed in support of the realization of the Sustainable Development Goals (SDGs), adopted by the United Nations in September 2015, which highlighted that economic growth must be inclusive, vulnerable populations - including women - must be specifically addressed, and that entrepreneurs will play a role in transforming economies for jobs and inclusive growth.
                <br>
                <br>More specifically, the international community agreed to “Achieve gender equality and empower all women and girls” (SDG 5), and to “Enhance the use of enabling technology, in particular information and communications technology, to promote the empowerment of women” (SDG 5, target 5.b). In this gender-based digital divide, women entrepreneurs face specific ICT-related challenges in the form of access to resources, information and training. The WIFI programme helps to address this gender digital divide through training, knowledge sharing and networking.
