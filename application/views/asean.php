        <h4><span>Description</span></h4>
      </div>
      <p><span class="highlight default">1</span> The Asian and Pacific Training Centre for ICT for Development (APCICT/ESCAP), in cooperation with the ASEAN Women Entrepreneurs Network (AWEN), Philippine Commission on Women (PCW), and Department of ICT of the Philippines (DICT) and with support from the ASEAN Secretariat, launched the Women ICT Frontier Initiative (WIFI) for the ASEAN Sub-Region on 28 August 2017 at the Philippine International Convention Centre (PICC) in Manila. </p>
      <p><span class="highlight default">2</span> The WIFI launch was held in conjunction with the ASEAN Women’s Business Conference and other related meetings took place from 28 August to 1 September 2017, in commemoration of the 50th Founding Anniversary of ASEAN. It was attended by high-level government officials and representatives of ICT and gender ministries from ASEAN countries as well as women entrepreneur associations, national government agencies, and other relevant stakeholders in the areas of entrepreneurship and women’s empowerment.</p>
      <p><span class="highlight default">3</span> Following the launch, a WIFI Training of Trainers (TOT) for policymakers and women entrepreneurs was organized on 29-30 August 2017. The TOT aimed to expand the pool of trainers and resource persons in Southeast Asia and who can help bring the WIFI training programme at the national and community levels.</p>

