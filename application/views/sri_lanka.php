          <h4><span>Description</span></h4>
        </div>
        <p><span class="highlight default">1</span> The Women ICT Frontier Initiative (WIFI), a flagship programme of the Asian and Pacific Training Centre for Information and Communication Technology for Development, was launched on 26 September 2016 in Colombo, Sri Lanka. </p>
        <p><span class="highlight default">2</span> WIFI is an ICT capacity development programme for promoting women’s entrepreneurship. In Sri Lanka, the programme is known as WIFI Suhuruliya or WIFI SMART Woman. It aims to promote the economic empowerment of women in Sri Lanka through the use of ICT in entrepreneurial activities. </p>
        <p><span class="highlight default">3</span> Organized in partnership with the Information and Communication Technology Agency (ICTA) of Sri Lanka, the WIFI launch was attended by around 80 senior government officials and stakeholders in entrepreneurship and women’s empowerment.</p>
